package com.example.dbtouching;

public class UsersToInstantiateThemselves {
	protected String firstName;
	protected String lastName; 
	protected String email;
	protected String StormTrooperID; 
	//---
	public String getFirstName() {
		return firstName;
	}
	public void setFirstName(String firstName) {
		this.firstName = firstName;
	}
	public String getLastName() {
		return lastName;
	}
	public void setLastName(String lastName) {
		this.lastName = lastName;
	}
	public String getEmail() {
		return email;
	}
	public void setEmail(String email) {
		this.email = email;
	}
	public String getStormTrooperID() {
		return StormTrooperID;
	}
	public void setStormTrooperID(String StormTrooperID) {
		this.StormTrooperID = StormTrooperID;
	}
	
}
